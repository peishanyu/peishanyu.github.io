function carouselHoroscopesModal() {

	var prdCarouselMask = document.getElementById("add-prd-carousel-Mask");
	if (prdCarouselMask.style.display === "none") {
		prdCarouselMask.style.display = "block";
	} else {
		prdCarouselMask.style.display = "none";
	};

	var modal_prdCarousel = document.getElementById("modal_carousel");
	if (modal_prdCarousel.style.display === "none") {
		modal_prdCarousel.style.display = "block";
	} else {
		modal_prdCarousel.style.display = "none";
	};

};









